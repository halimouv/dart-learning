void main() {
   // Dart Basics | 5 - Assignment Operation
   var x = 10;
   x = x+1;        // x=11
   x--;            // x=10
   x++;            // x=11
   x+= 5;          // x=16
   x-= 6;          // x=10
   x = x-5;        // x=5
   x *= 4;         // x=20

   // to divide numbers convert variable to double
   double y = x.toDouble();
   y /= 2;         // y=10
   print(x);
   print(y);
}

void main() {
   // Dart Basics | 4 - Operation
   var num1 = 2025;
   var num2 = 2034;

   // Operations on Dart
   // Basic Math
   var result;
   result = num1 + num2;
   result = num1 - num2;
   result = num1 * num2;
   result = num1 / num2;
   result = num1 % num2;
   print(result);

   // Booleans (returns true/false values)
   result = num1 > num2;
   result = num1 < num2;
   result = num1 == num2;
   result = num1 != num2;
   result = num1 >= num2;
   result = num1 <= num2;
   result = num1 <= 2020 ||  num2 <= 2030;
   result = num1 < 2050 && num2 > 2020;
   print(result);
}
